//! A collection of [LinearRgba] colors that can be used in a const context.

use bevy::color::LinearRgba;

pub const fn hex_color(hex: u32) -> LinearRgba {
    let r = (hex >> 16) & 0xff;
    let g = (hex >> 8) & 0xff;
    let b = hex & 0xff;
    LinearRgba::new(r as f32 / 255.0, g as f32 / 255.0, b as f32 / 255.0, 1.0)
}

